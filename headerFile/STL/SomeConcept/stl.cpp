// STL 六大组件 :

// 容器 Container : 各种存放数据的数据结构 , 如 向量vector, 列表list ,双端队列deque , 集合set , 映射map 等 ;
// 算法 Algorithm : 各种应用于容器 Container 上的常用算法, 如: 排序算法Sort , 搜索算法Search, 拷贝算法Copy, 删除算法 Erase 等 ;
// 迭代器 Iterator : 容器 与 算法 之间 通过 迭代器 进行关联 , 其本质 是 泛型指针 ;
// 仿函数 Function Object : 函数对象 , 可以作为参数进行传递 , 可以作为算法的某种策略 ;
// 适配器 Adaptor : 用于修饰容器, 仿函数 , 迭代器 接口 ;
// 空间配制器 Allocator : 负责空间配置与管理 ;
